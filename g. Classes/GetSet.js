/**
 * ---getter e setter----
 * 
 * ** Caso não se defina métodos setter e getter numa propriedade, ela terá um comportamentro padrão. 
 * Ao chamar irá executar o get(), e ao atribuir será executado o set()/
 * 
 * ** É importante lembrar que as propriedades podem ser acessadas diretamente e modificadas sem qualquer validação. Isso pode ser problemático se você precisar garantir que as propriedades de um objeto mantenham um certo estado ou validação. Nesses casos, é útil definir métodos get() e set() personalizados para essas propriedades, permitindo que você adicione validação personalizada ou execute outras ações conforme necessário.**
 * 
 */